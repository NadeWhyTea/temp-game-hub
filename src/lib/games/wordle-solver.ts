export type LetterState = 'correct' | 'present' | 'absent' | 'empty';

export interface WordleGuess {
  word: string;
  states: LetterState[];
}

export interface WordleState {
  targetWord: string;
  guesses: WordleGuess[];
  currentGuess: string;
  isGameOver: boolean;
  isWon: boolean;
  possibleWords: string[];
}

export function createWordleGame(): WordleState {
  const targetWord = WORDLE_WORDS[Math.floor(Math.random() * WORDLE_WORDS.length)];
  
  return {
    targetWord,
    guesses: [],
    currentGuess: '',
    isGameOver: false,
    isWon: false,
    possibleWords: [...WORDLE_WORDS],
  };
}

export function evaluateGuess(guess: string, targetWord: string): LetterState[] {
  const states: LetterState[] = ['empty', 'empty', 'empty', 'empty', 'empty'];
  const targetLetters = targetWord.split('');
  const guessLetters = guess.split('');
  
  // First pass: mark correct positions
  for (let i = 0; i < 5; i++) {
    if (guessLetters[i] === targetLetters[i]) {
      states[i] = 'correct';
      targetLetters[i] = '_';
      guessLetters[i] = '_';
    }
  }
  
  // Second pass: mark present letters
  for (let i = 0; i < 5; i++) {
    if (guessLetters[i] !== '_' && targetLetters.includes(guessLetters[i])) {
      states[i] = 'present';
      targetLetters[targetLetters.indexOf(guessLetters[i])] = '_';
    }
  }
  
  // Third pass: mark absent letters
  for (let i = 0; i < 5; i++) {
    if (guessLetters[i] !== '_' && states[i] === 'empty') {
      states[i] = 'absent';
    }
  }
  
  return states;
}

export function filterPossibleWords(
  possibleWords: string[],
  guesses: WordleGuess[]
): string[] {
  return possibleWords.filter(word => {
    for (const guess of guesses) {
      // Check if word is consistent with all guesses
      for (let i = 0; i < 5; i++) {
        const guessLetter = guess.word[i];
        const wordLetter = word[i];
        const state = guess.states[i];
        
        if (state === 'correct' && wordLetter !== guessLetter) {
          return false;
        }
        if (state === 'absent' && word.includes(guessLetter)) {
          // Make sure the letter isn't in a position marked as 'present'
          let hasPresent = false;
          for (let j = 0; j < 5; j++) {
            if (guess.states[j] === 'present' && guess.word[j] === guessLetter) {
              hasPresent = true;
              break;
            }
          }
          if (!hasPresent && word.includes(guessLetter)) {
            return false;
          }
        }
        if (state === 'present' && !word.includes(guessLetter)) {
          return false;
        }
        if (state === 'present' && wordLetter === guessLetter) {
          // Letter is present but not in this position
          return false;
        }
      }
    }
    return true;
  });
}

export function submitGuess(state: WordleState, guess: string): WordleState {
  if (state.isGameOver || guess.length !== 5 || !WORDLE_WORDS.includes(guess)) {
    return state;
  }
  
  const states = evaluateGuess(guess, state.targetWord);
  const newGuess: WordleGuess = { word: guess, states };
  const newGuesses = [...state.guesses, newGuess];
  
  const isWon = guess === state.targetWord;
  const isGameOver = isWon || newGuesses.length >= 6;
  
  const newPossibleWords = filterPossibleWords(state.possibleWords, newGuesses);
  
  return {
    ...state,
    guesses: newGuesses,
    currentGuess: '',
    isGameOver,
    isWon,
    possibleWords: newPossibleWords,
  };
}

export function addLetter(state: WordleState, letter: string): WordleState {
  if (state.currentGuess.length < 5 && !state.isGameOver) {
    return {
      ...state,
      currentGuess: state.currentGuess + letter,
    };
  }
  return state;
}

export function removeLetter(state: WordleState): WordleState {
  if (state.currentGuess.length > 0) {
    return {
      ...state,
      currentGuess: state.currentGuess.slice(0, -1),
    };
  }
  return state;
}

export function getKeyboardState(guesses: WordleGuess[]): Map<string, LetterState> {
  const keyboardState = new Map<string, LetterState>();
  
  for (const guess of guesses) {
    for (let i = 0; i < 5; i++) {
      const letter = guess.word[i];
      const state = guess.states[i];
      
      // Update keyboard state with the best (most informative) state for each letter
      const currentState = keyboardState.get(letter);
      if (!currentState || 
          (currentState === 'absent' && state !== 'absent') ||
          (currentState === 'present' && state === 'correct')) {
        keyboardState.set(letter, state);
      }
    }
  }
  
  return keyboardState;
}
